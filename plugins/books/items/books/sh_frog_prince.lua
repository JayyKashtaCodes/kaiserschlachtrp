<font color='white' size='6'>The Frog Prince</font>
<font color='grey' size='4'>Brothers Grimm</font>

In old times when wishing still helped, there lived a king whose daughters were all beautiful, but the youngest was so beautiful that the sun itself, which had seen so much, marveled every time it shone upon her face.

Near the royal castle lay a dark, gloomy forest, and in the forest under an old lime tree was a well, and when the day was very warm, the king's child went out into the forest and sat down by the side of the cool fountain; and when she was bored she took a golden ball, and threw it up on high and caught it; and this ball was her favorite plaything.

Now it so happened that on one occasion the princess's golden ball did not fall into her hand but onto the ground, and rolled straight into the water. The king's daughter followed it with her eyes, but it vanished, and the well was deep, so deep that the bottom could not be seen.

On this she began to cry, and cried louder and louder, and could not be comforted. And as she thus lamented someone said to her, "What ails you, king's daughter? You weep so that even a stone would show pity."

She looked round to the side from whence the voice came, and saw a frog stretching forth its thick, ugly head from the water. "Ah, old water-splasher, is it you," she said, "I am weeping for my golden ball, which has fallen into the well."

"Be quiet, and do not weep," answered the frog, "I can help you; but what will you give me if I bring your plaything up again?"

"Whatever you will have, dear frog," said she, "My clothes, my pearls and jewels, and even the golden crown which I am wearing."

The frog answered, "I do not care for your clothes, your pearls and jewels, nor for your golden crown; but if you will love me and let me be your companion and playfellow, and sit by you at your little table, and eat off your little golden plate, and drink out of your little cup, and sleep in your little bed - if you will promise me this, I will go down below, and bring you your golden ball up again."

"Oh yes," said she, "I promise you all you wish, if you will but bring me my ball back again." She, however, thought, "How the silly frog does talk! All he does is to sit in the water with the other frogs, and croak; he can be no companion to any human being!"

But the frog, when he had received this promise, put his head into the water and sank down; and in a short while came swimming up again with the ball in his mouth, and threw it on the grass. The king's daughter was delighted to see her pretty plaything once more, picked it up, and ran away with it.

"Wait, wait," said the frog. "Take me with you. I can't run as you can." But what did it avail him to scream his croak, croak, after her, as loudly as he could? She did not listen to it, but ran home and soon forgot the poor frog, who was forced to go back into his well again.

The next day when she had seated herself at table with the king and all the courtiers, and was eating from her little golden plate, something came creeping, splish splash, splish splash, up the marble staircase, and when it had got to the top, it knocked at the door and cried, "Princess, youngest princess, open the door for me."

She ran to see who was outside. But when she opened the door, there sat the frog in front of it.

Then she slammed the door shut and hastily returned to the table, feeling quite uneasy. The king saw her heart was beating so heavily, and asked, "My child, what are you so afraid of? Is there perhaps a giant outside who wants to carry you away?"

"No," she replied, "It is no giant but a disgusting frog."

"What does the frog want with you?"

"Oh, dear father, yesterday when I was sitting by the well, playing, my golden ball fell into the water, and because I was crying so, the frog brought it out again for me. But he wanted me to promise to let him be my companion, and I never thought he would be able to come out of the well. And now he is outside there and wants to come in to me."

Then the frog knocked at the door a second time, and cried, "Princess, youngest princess, open the door for me, do you not remember yesterday? By the cool waters bright, you said I'd be your partner, and let me sit beside you at your table."

The king said, "That which you have promised must you perform. Go and let him in."

She went and opened the door for the frog. He hopped in and followed her to her chair. There he sat and cried, "Lift me up beside you."

She did not dare to do so, but the king ordered her to do it.

When the frog was on the chair, he wanted to be on the table, and when he was on the table he said, "Now, push your little golden plate near to me so that we can eat together."

She did this, but it was easy to see that she did not do it willingly. The frog enjoyed what he ate, but almost every mouthful she took choked her.

At length he said, "I have eaten and am satisfied; now I am tired, carry me into your little room and make your little silken bed ready, and we will both lie down and go to sleep."

The king's daughter began to cry, for she was afraid of the cold frog which she did not like to touch and which was now to sleep in her pretty, clean little bed.

But the king grew angry and said, "He who helped you when you were in trouble ought not afterwards to be despised by you."

So she took hold of the frog with two fingers, carried him upstairs, and put him in a corner. But when she was in bed, he crept to her and said, "I am tired, I want to sleep as well as you, lift me up, or I will tell your father."

Then she was terribly angry, took him up and threw him with all her might against the wall.

"Now you will be quiet, odious frog," said she.

But when he fell down he was no frog but a king's son with beautiful kind eyes. He was now, according to her father's will, her dear companion and husband.

Then he told her how he had been bewitched by a wicked witch and how no one could have delivered him from the well but herself, and that they both went together to his kingdom, and there they lived happily ever after.
